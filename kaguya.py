# --------- 页面设置 ----------
def gif_html(path, width=200):
    data = open(path, "rb").read()
    b64 = base64.b64encode(data).decode("utf-8")
    return f'<img src="data:image/gif;base64,{b64}" width="{width}">'

st.markdown(gif_html("yachiyo.gif", width=5000), unsafe_allow_html=True)
st.set_page_config(page_title="AI-Yachiyo (Kaguya)", page_icon="🌙", layout="centered")
