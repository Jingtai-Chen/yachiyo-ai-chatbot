# --------- 读取 API Key ----------
api_key = os.getenv("OPENAI_API_KEY")
if not api_key:
    st.error("没检测到 OPENAI_API_KEY。\n\n请在项目根目录创建 .env，并写入：\nOPENAI_API_KEY=你的key")
    st.stop()

client = OpenAI(api_key=api_key)

avatar = Image.open("yachiyo.png")

with st.sidebar:

    st.image(avatar, width=200)
    st.markdown("# 月見 ヤチヨ")
    st.header("⚙️ 设置")
    
    model = st.selectbox(
        "模型",
        options=["gpt-4o-mini", "gpt-4.1-mini","gpt-4.1-2025-04-14"],
        index=2,
        help="先用 gpt-4o-mini：便宜、快、适合 Demo"
    )

    temperature = st.slider(
        "回答随机度 (temperature)",
        min_value=0.0,
        max_value=1.2,
        value=0.6,
        step=0.1,
        help="调低：像认真负责的八千代 📘｜调高：像话多可爱的辉夜姬 🌙"
    )

#
    if st.button("🧹 清空对话", use_container_width=True):
        st.session_state.messages = [{ "role": "system", "content": SYSTEM_PROMPT }]
        st.rerun()

    st.divider()
    st.markdown("**提示**：不要把 API Key 发到任何地方；确保 `.env` 在 `.gitignore` 里。")

# --------- 初始化对话 ----------
if "messages" not in st.session_state:
    st.session_state.messages = [{ "role": "system", "content": SYSTEM_PROMPT }]

# --------- 展示历史消息（跳过 system） ----------
for m in st.session_state.messages:
    if m["role"] == "system":
        continue
    with st.chat_message(m["role"]):
        st.markdown(m["content"])

# --------- 输入框 ----------
user_input = st.chat_input("跟我说点什么吧…（比如：よね）")


# --------- 发送并获取回复 ----------
if user_input:
    # 1) 记录用户输入
    st.session_state.messages.append({"role": "user", "content": user_input})
    with st.chat_message("user"):
        st.markdown(user_input)

    # 2) 调用模型
    with st.chat_message("assistant", avatar="yachiyo.png"):
        placeholder = st.empty()
        placeholder.markdown("思考中…")

        try:
            resp = client.chat.completions.create(
                model=model,
                temperature=temperature,
                messages=st.session_state.messages,
            )
            answer = resp.choices[0].message.content
        except Exception as e:
            answer = f"⚠️ 出错了：{e}"

        placeholder.markdown(answer)

    # 3) 记录助手回复
    st.session_state.messages.append({"role": "assistant", "content": answer})